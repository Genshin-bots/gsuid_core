"""Global Selection 提示词

用于 System-2 分层图遍历中，LLM 在候选 Category 列表中
选择与 query 相关的节点的提示词模板。
"""

NODE_SELECTION_PROMPT_TEMPLATE = """你正在分析一个知识图谱，以帮助回答用户查询。

用户查询："{query}"

从以下可用节点中，选出所有可能有助于回答查询的节点。
一个节点有用如果它：
- 与查询直接相关
- 覆盖明确相关的话题、概念或类别
- 提供有用的背景或上下文
- 包含用户特定信息（如兴趣、目标、约束）
- 可能有有用的子节点

不要过于严格：即使部分冗余，只要能提供上下文，也应选择。

**输出格式严格要求（使用简写键名）**：
- n: 节点名称
- u: 节点的唯一标识符
- g: 布尔值，仅当确信该节点的所有子节点都有用时设为 true（早停优化）

可用节点：
{nodes_info}

输出格式（纯 JSON 数组）：
[
  {{"n": "...", "u": "...", "g": false}},
  ...
]
"""
